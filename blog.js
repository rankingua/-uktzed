function buildBlog(ctx) {
  const { OUT, fs, path, head, foot, esc, site } = ctx;

  // ── Сторінка-список /blog/ ──
  const listCanonical = `${site.domain}/${BLOG_SLUG}/`;
  const cards = ARTICLES.map(a => `
    <a class="bcard" href="/${BLOG_SLUG}/${a.slug}/">
      <span class="bcard-title">${esc(a.h1)}</span>
      <span class="bcard-desc">${esc(a.description)}</span>
    </a>`).join("");

  const listBreadcrumb = {
    "@context": "https://schema.org", "@type": "BreadcrumbList",
    itemListElement: [
      { "@type": "ListItem", position: 1, name: "Головна", item: site.domain + "/" },
      { "@type": "ListItem", position: 2, name: "Блог", item: listCanonical }
    ]
  };

  const listBody = head(
    "Блог про розмитнення та коди УКТЗЕД — Тарифник",
    "Статті про розмитнення посилок, коди УКТЗЕД, мито та ПДВ простими словами. Практичні гайди для імпортерів і покупців із-за кордону.",
    listCanonical, "website"
  ) + `
  <nav class="crumbs"><a href="/">Головна</a> → Блог</nav>
  <article>
    <h1>Блог</h1>
    <p class="lead">Практичні гайди про розмитнення, коди УКТЗЕД, мито та ПДВ — простою мовою, з посиланнями на закони.</p>
    <div class="bgrid">${cards}</div>
  </article>
  <script type="application/ld+json">${JSON.stringify(listBreadcrumb)}</script>
  ` + foot;

  const blogDir = path.join(OUT, BLOG_SLUG);
  fs.mkdirSync(blogDir, { recursive: true });
  fs.writeFileSync(path.join(blogDir, "index.html"), listBody);

  // ── Сторінки статей /blog/<slug>/ ──
  ARTICLES.forEach(a => {
    const canonical = `${site.domain}/${BLOG_SLUG}/${a.slug}/`;
    const secHtml = a.sections.map(s =>
      `<section><h2 class="blog-h2">${esc(s.h)}</h2>${s.html}</section>`
    ).join("\n");
    const faqHtml = a.faq.map(f =>
      `<div class="faq-item"><h3>${esc(f.q)}</h3><p>${esc(f.a)}</p></div>`).join("");
    const relatedHtml = a.related.map(r =>
      `<li><a href="/${r.slug}/">${esc(r.nazva)}</a></li>`).join("");
    const sourcesHtml = (a.sources || []).map(s =>
      `<li><a href="${s.url}" target="_blank" rel="noopener">${esc(s.name)}</a></li>`).join("");

    const faqLd = {
      "@context": "https://schema.org", "@type": "FAQPage",
      mainEntity: a.faq.map(f => ({
        "@type": "Question", name: f.q,
        acceptedAnswer: { "@type": "Answer", text: f.a }
      }))
    };
    const articleLd = {
      "@context": "https://schema.org", "@type": "Article",
      headline: a.h1, description: a.description, inLanguage: "uk-UA",
      datePublished: a.datePub, dateModified: a.dateMod,
      mainEntityOfPage: { "@type": "WebPage", "@id": canonical },
      author: { "@type": "Organization", name: site.name, url: site.domain + "/" },
      publisher: { "@type": "Organization", name: site.name, url: site.domain + "/" }
    };
    const breadcrumb = {
      "@context": "https://schema.org", "@type": "BreadcrumbList",
      itemListElement: [
        { "@type": "ListItem", position: 1, name: "Головна", item: site.domain + "/" },
        { "@type": "ListItem", position: 2, name: "Блог", item: `${site.domain}/${BLOG_SLUG}/` },
        { "@type": "ListItem", position: 3, name: a.h1, item: canonical }
      ]
    };

    const body = head(a.title, a.description, canonical, "article") + `
  <nav class="crumbs"><a href="/">Головна</a> → <a href="/${BLOG_SLUG}/">Блог</a> → ${esc(a.h1)}</nav>
  <article class="blog-article">
    <h1>${esc(a.h1)}</h1>
    <p class="lead">${esc(a.lead)}</p>
    ${secHtml}
    ${relatedHtml ? `<section>
      <h2 class="blog-h2">Читайте також</h2>
      <ul class="related-links">${relatedHtml}</ul>
    </section>` : ""}
    ${sourcesHtml ? `<section>
      <h2 class="blog-h2">Джерела</h2>
      <ul class="source-links">${sourcesHtml}</ul>
    </section>` : ""}
    <p class="blog-disclaimer">Матеріал має довідковий характер і не є офіційним роз'ясненням митниці. Ставки й правила можуть змінюватися — перед розмитненням звіряйте дані з чинним Митним тарифом України або консультуйтеся з митним брокером.</p>
  </article>
  <script type="application/ld+json">${JSON.stringify(articleLd)}</script>
  <script type="application/ld+json">${JSON.stringify(faqLd)}</script>
  <script type="application/ld+json">${JSON.stringify(breadcrumb)}</script>
  ` + foot;

    const dir = path.join(blogDir, a.slug);
    fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(path.join(dir, "index.html"), body);
  });
}

// список URL для sitemap
function blogUrls() {
  return [BLOG_SLUG + "/", ...ARTICLES.map(a => `${BLOG_SLUG}/${a.slug}/`)];
}

module.exports = { buildBlog, blogUrls, BLOG_SLUG, ARTICLES };
